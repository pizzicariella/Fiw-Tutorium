package klausurvorbereitung.linien;

import java.awt.Point;

public class Linie {
	private Point start, end;

	public Point getStart() {
		return start;
	}

	public void setStart(Point start) {
		this.start = start;
	}

	public Point getEnd() {
		return end;
	}

	public void setEnd(Point end) {
		this.end = end;
	}
}
